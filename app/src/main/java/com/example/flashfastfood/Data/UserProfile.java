package com.example.flashfastfood.Data;

public class UserProfile {
    public String FullName, Email, Gender, PhoneNumber;

    public UserProfile(){}

    public UserProfile(String fullName, String email, String gender, String phoneNumber) {
        FullName = fullName;
        Email = email;
        Gender = gender;
        PhoneNumber = phoneNumber;
    }

}
